const myDate = new Date() 
console.log(myDate);
console.log(myDate.toDateString());

console.log(myDate.getFullYear());  //Get year as a four digit number (yyyy)
console.log(myDate.getMonth());  //Get month as a number (0-11)
console.log(myDate.getDate());  //Get day as a number (1-31)
console.log(myDate.getDay());   //Get weekday as a number (0-6)
console.log(myDate.getHours());  //Get hour (0-23)
console.log(myDate.getMinutes());   //Get minute (0-59)
console.log(myDate.getSeconds());   //Get second (0-59)
console.log(myDate.getMilliseconds());  //Get millisecond (0-999)
console.log(myDate.getTime());  //Get time (milliseconds since January 1, 1970)



